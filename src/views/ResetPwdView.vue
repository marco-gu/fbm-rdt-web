<template>
  <div class="wrapper">
    <div class="header">
      <q-item clickable>
        <q-item-section avatar @click="back">
          <q-icon name="arrow_back" />
        </q-item-section>
        <q-item-section>
          <span class="header-title">{{ changePwdTitle }}</span></q-item-section
        >
      </q-item>
    </div>
    <q-separator />
    <q-card class="content-card">
      <q-form @submit="onSubmit">
        <div class="content-input">
          <q-input
            v-model="currentPwd"
            filled
            :type="isPwd ? 'password' : 'text'"
            :placeholder="currentPwdLabel"
            lazy-rules
            :rules="[initPwdRule]"
          >
            <template v-slot:append>
              <q-icon
                :name="isPwd ? 'visibility_off' : 'visibility'"
                class="cursor-pointer"
                @click="isPwd = !isPwd"
              />
            </template>
          </q-input>
          <q-input
            v-model="newPwd"
            filled
            :type="isPwd ? 'password' : 'text'"
            :placeholder="newPwdLabel"
            lazy-rules
            :rules="[newPwdRule]"
          >
            <template v-slot:append>
              <q-icon
                :name="isPwd ? 'visibility_off' : 'visibility'"
                class="cursor-pointer"
                @click="isPwd = !isPwd"
              />
            </template>
          </q-input>
          <q-input
            v-model="reNewPwd"
            filled
            :type="isPwd ? 'password' : 'text'"
            :placeholder="retypeNewPwdLabel"
            lazy-rules
            :rules="[reNewPwdRule]"
          >
            <template v-slot:append>
              <q-icon
                :name="isPwd ? 'visibility_off' : 'visibility'"
                class="cursor-pointer"
                @click="isPwd = !isPwd"
              />
            </template>
          </q-input>
        </div>
        <div class="bottom">
          <q-btn
            no-caps
            style="width: 48%"
            flat
            push
            :label="cancelLabel"
            @click="cancel"
          />
          <q-separator vertical inset color="white" />
          <q-btn
            no-caps
            style="width: 52%"
            flat
            type="submit"
            push
            :label="saveLabel"
          />
        </div>
      </q-form>
    </q-card>
  </div>
</template>
<script lang="ts">
import {
  AndroidResponse,
  AndroidResponseStatus,
} from "@/models/android.response";
import { LogoutResponse, UpdateLoginUser } from "@/models/login.response";
import bridge from "dsbridge";
import { useQuasar } from "quasar";
import { defineComponent, onMounted, ref } from "vue";
import { useRoute, useRouter } from "vue-router";
import md5 from "md5";
import { popupErrorMsg, popupSuccessMsg } from "@/plugin/popupPlugins";
import { useI18n } from "@/plugin/i18nPlugins";
import { closeLoading, showLoading } from "@/plugin/loadingPlugins";

const ResetPwdView = defineComponent({
  setup() {
    const route = useRoute();
    const router = useRouter();
    const $q = useQuasar();
    const i18n = useI18n();
    const currentPwd = ref("");
    const newPwd = ref("");
    const reNewPwd = ref("");
    const username = ref("");
    const initPwd = ref();
    const from = ref("");
    const isPwd = ref(true);
    const changePwdTitle = ref("");
    const currentPwdLabel = ref("");
    const newPwdLabel = ref("");
    const retypeNewPwdLabel = ref("");
    const cancelLabel = ref("");
    const saveLabel = ref("");
    onMounted(() => {
      from.value = route.params.from as string;
      username.value = route.params.username as string;
      initPwd.value = route.params.password as string;
      bridge.call("getSettingLanguage", null, (res: string) => {
        i18n.locale.value = res;
        i18n.category.value = "ResetPwdView";
        changePwdTitle.value = i18n.$t("changePwd");
        currentPwdLabel.value = i18n.$t("currentPwd");
        newPwdLabel.value = i18n.$t("newPwd");
        retypeNewPwdLabel.value = i18n.$t("retypeNewPwd");
        cancelLabel.value = i18n.$t("cancel");
        saveLabel.value = i18n.$t("save");
      });
    });
    const onSubmit = () => {
      showLoading($q);
      const args: UpdateLoginUser = {
        username: username.value,
        password: md5(newPwd.value),
        autoGeneratedPassword: false,
      };
      bridge.call("resetPassword", args, (res: string) => {
        closeLoading($q);
        const androidResponse = JSON.parse(res) as AndroidResponse<unknown>;
        if (androidResponse.status == AndroidResponseStatus.SUCCESS) {
          const message = i18n.$t("E93-02-0001");
          if (from.value == "LoginView") {
            router.push("/home");
          } else if (from.value == "SettingView") {
            router.push("/setting");
          }
          popupSuccessMsg($q, message);
        } else if (androidResponse.status == AndroidResponseStatus.ERROR) {
          const message = i18n.$t(androidResponse.messageCode);
          popupErrorMsg($q, message);
        }
      });
    };
    const initPwdRule = (val: string) => {
      i18n.category.value = "MessageCode";
      return new Promise((resolve) => {
        if (!val) {
          const message = i18n.$t("E93-02-0005");
          resolve(message);
        } else {
          if (from.value === "SettingView") {
            if (md5(currentPwd.value) != initPwd.value) {
              const message = i18n.$t("E93-02-0003");
              resolve(message);
            } else {
              resolve(true);
            }
          } else {
            if (currentPwd.value != initPwd.value) {
              const message = i18n.$t("E93-02-0003");
              resolve(message);
            } else {
              resolve(true);
            }
          }
        }
      });
    };
    const newPwdRule = (val: string) => {
      i18n.category.value = "MessageCode";
      return new Promise((resolve) => {
        const reg = /[A-Z]+/g;
        const reg2 = /[0-9]+/g;
        if (!val) {
          const message = i18n.$t("E93-02-0006");
          resolve(message);
        } else {
          if (newPwd.value.length < 8) {
            const message = i18n.$t("E93-02-0004");
            resolve(message);
          } else {
            if (!reg.test(newPwd.value)) {
              const message = i18n.$t("E93-02-0004");
              resolve(message);
            } else if (!reg2.test(newPwd.value)) {
              const message = i18n.$t("E93-02-0004");
              resolve(message);
            } else {
              resolve(true);
            }
          }
        }
      });
    };
    const reNewPwdRule = (val: string) => {
      i18n.category.value = "MessageCode";
      return new Promise((resolve) => {
        if (!val) {
          const message = i18n.$t("E93-02-0007");
          resolve(message);
        } else {
          if (newPwd.value != reNewPwd.value) {
            const message = i18n.$t("E93-02-0002");
            resolve(message);
          } else {
            resolve(true);
          }
        }
      });
    };
    const cancel = () => {
      if (from.value == "LoginView") {
        bridge.call("logout", null, (data: string) => {
          const androidResponse = JSON.parse(
            data
          ) as AndroidResponse<LogoutResponse>;
          if (androidResponse.status == AndroidResponseStatus.SUCCESS) {
            const message = i18n.$t("E93-06-0001");
            router.push("/");
            popupSuccessMsg($q, message);
          }
        });
      } else if (from.value == "SettingView") {
        router.push("/setting");
      }
    };
    const back = () => {
      if (from.value == "LoginView") {
        cancel();
      } else if (from.value == "SettingView") {
        router.push("/setting");
      }
    };
    return {
      isPwd,
      onSubmit,
      cancel,
      back,
      currentPwd,
      newPwd,
      reNewPwd,
      initPwdRule,
      newPwdRule,
      reNewPwdRule,
      changePwdTitle,
      currentPwdLabel,
      newPwdLabel,
      retypeNewPwdLabel,
      cancelLabel,
      saveLabel,
    };
  },
});
export default ResetPwdView;
</script>
